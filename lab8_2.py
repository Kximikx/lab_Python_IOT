def ways_to_step(n: int) -> int: 
    if n < 0:  
        return 0  

    dp = [0] * (n + 1)  # dp[i] = кількість способів дійти до i
    dp[0] = 1  # базово: до 0-ї сходинки є 1 спосіб (стояти на старті)

    for i in range(1, n + 1):  # рахуємо dp для всіх i від 1 до n
        dp[i] = dp[i - 1]  # крок на 1: всі способи з i-1 переходять у i
        if i - 4 >= 0:  # якщо можна зробити крок на 4
            dp[i] += dp[i - 4]  # додаємо способи з i-4 (стрибок на 4)

    return dp[n]  # відповідь для n


n = int(input().strip())  # зчитуємо n
ans = ways_to_step(n)  # рахуємо кількість способів

print("Введена сходинка n:", n)  
print("Кількість способів піднятися:", ans)  